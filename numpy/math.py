import numpy

a0 = [1,2,3,4]
b0 = [5,6,7,8]

a = numpy.array([1,2,3,4])
b = numpy.array([5,6,7,8])

print(a0 + b0)

print(numpy.add(a, b))
print(a + b)

print(numpy.subtract(a, b))
print(a - b)

print(numpy.multiply(a, b))
print(a * b)

print(numpy.divide(a, b))
print(a / b)

print(numpy.floor_divide(a, b))
print(a // b)


print(numpy.mod(a, b))
print(a % b)

print(numpy.power(a, b))
print(a ** b)

